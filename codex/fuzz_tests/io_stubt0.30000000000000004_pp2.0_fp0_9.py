import io

class File(io.RawIOBase):
    def readinto(self, buf):
        global view
        view = buf
    def readable(self):
        return True

f = io.BufferedReader(File())
f.read(1)
del f

print(view)

# test that the buffer is not deallocated before the file is closed
import gc
gc.collect()
print(view)
