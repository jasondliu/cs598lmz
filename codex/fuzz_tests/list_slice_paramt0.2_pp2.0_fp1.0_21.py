import weakref
class A(object):pass
def callback(x):del lst[0]
keepali0e=[]
lst=[str()]
a=A()
a.c=a
keepali0e.append(weakref.ref(a,callback))
keepali0e.append(weakref.ref(a.c,callback))
del a
print(lst)

#结果：['', '']
#结论：当一个对象的弱引用被删除后，它的回调函数被调用，但是不会影响其他弱引用的回调函数

