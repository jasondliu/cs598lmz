import mmap
# Test mmap.mmap(0, 1)
m = mmap.mmap(-1, 1)
m.write(b"\x00")
m.close()
# Test mmap.mmap(-1, 1, access=mmap.ACCESS_WRITE)
m = mmap.mmap(-1, 1, access=mmap.ACCESS_WRITE)
m.write(b"\x00")
m.close()
# Test mmap.mmap(0, 1, access=mmap.ACCESS_READ)
m = mmap.mmap(0, 1, access=mmap.ACCESS_READ)
m.seek(0)
m.read(1)
m.close()
# Test mmap.mmap(-1, 1, access=mmap.ACCESS_COPY)
m = mmap.mmap(-1, 1, access=mmap.ACCESS_COPY)
m.seek(0)
m.read(1)
m.close()
