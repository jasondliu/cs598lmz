class Sensor:
    #---------------------------------------------------------------------------
    #
    #---------------------------------------------------------------------------
    def __init__(self, name, host, port):
        self.name = name
        self.host = host
        self.port = port

    #---------------------------------------------------------------------------
    #
    #---------------------------------------------------------------------------
    def open(self):
        self.sock = socket.socket()
        self.sock.connect((self.host, self.port))

    #---------------------------------------------------------------------------
    #
    #---------------------------------------------------------------------------
    def close(self):
        self.sock.close()

    #---------------------------------------------------------------------------
    #
    #---------------------------------------------------------------------------
    def readline(self):
        data = ''
        while True:
            c = self.sock.recv(1)
            if c == '\n':
                return data
            else:
                data += c

