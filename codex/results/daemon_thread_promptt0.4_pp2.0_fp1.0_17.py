import threading
# Test threading daemon
# https://stackoverflow.com/questions/16202811/python-threading-timer-repeat-function-every-n-seconds

def foo():
    print("Hello")

t = threading.Timer(5.0, foo)
t.start()  # after 30 seconds, "hello, world" will be printed

print("Hello world!")

