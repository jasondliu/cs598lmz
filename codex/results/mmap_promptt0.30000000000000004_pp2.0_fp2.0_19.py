import mmap
# Test mmap.mmap()
f = open('test.txt', 'r+')
m = mmap.mmap(f.fileno(), 0)
print(m.readline())
print(m.readline())

m.seek(0)
m.write('0123456789abcdef')
m.seek(0)
print(m.readline())
m.close()
f.close()

