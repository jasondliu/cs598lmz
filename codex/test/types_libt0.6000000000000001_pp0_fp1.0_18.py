# 클래스 선언
class MyClass:
    # 속성
    var1 = 1
    var2 = 2
    var3 = 3
    # 메소드
    def add(self):
        return self.var1 + self.var2 + self.var3
    
# 객체 생성
obj = MyClass()
print('var1:', obj.var1)
print('var2:', obj.var2)
print('var3:', obj.var3)
print('add:', obj.add())

